 class P12  
{    
public static void main(String args[])   
{    
    
 
int i, j, c = 5;       

for (i=0; i<c; i++)   
{  
      
for (j=2*(c-i); j>=0; j--)         
{  
     
System.out.print(" ");   
}   
  
for (j=0; j<=i; j++ )   
{   
    
System.out.print("* ");   
}   
 
System.out.println();   
}   
}   
}  